function norm = norm_Q(y, M, Mt)

% =========================================================================
% Author: Sophia Kohle, Technische Universit√§t Berlin
% =========================================================================
%
% Task: Computation of ||y||_L^2(Q)
%              
% -------------------------------------------------------------------------
% -------------------------------------------------------------------------
%                
% Input parameters:
%
% y        % function
% M        % mass matrix
% Mt       
% 
% -------------------------------------------------------------------------
% -------------------------------------------------------------------------
%                
% Output:   norm = ||y||_L^2(Q)
% =========================================================================
n = size(y,2);

norm_Omega = zeros(n,1);
for i =1:n
    norm_Omega(i) = sqrt(y(:,i)'*M*y(:,i));
end
norm = sqrt(norm_Omega'*Mt*norm_Omega); 