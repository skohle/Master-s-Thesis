function error = error_numerical_0T(u1, u2, Mt)

% =========================================================================
% Author: Sophia Kohle, Technische Universität Berlin
% =========================================================================
%
% Task: Computation of the numerical L²(Omega x 0,T) - distance between two 
%       controls u1, u2
%
%       ||u1 - u2||_L^2(0,T)
%              
% -------------------------------------------------------------------------
% -------------------------------------------------------------------------
%                
% Input parameters:
%
% u1        % control 1
% u2        % control 2
% Mt
% 
% -------------------------------------------------------------------------
% -------------------------------------------------------------------------
%                
% Output:   distance between u1 and u2
% =========================================================================
error = sqrt((u1-u2)'*Mt*(u1-u2)); 